<h2>Honoring a 'quietly incredible woman'</h2>
</div>
<div class="bodycontent">
  <p>When Mark Headley (Stevenson '83, politics) and his wife, Christina Pehl, decided to make a gift establishing a new endowed chair at UCSC, they wanted to commemorate an extraordinary woman who worked tirelessly for four University of California presidents.<br />
    <br />
    That's how the Dorothy E. Everett Chair was born.<br />
    <br />
    &quot;I knew her as a friend and as a virtual family member,&quot; Headley said. Everett was a beloved and trusted co-worker of Headley's father.<br />
    <br />
    &quot;Early evening, about 8:30 p.m., after finishing the dinner dishes, my father would get on the phone with her to discuss the business of the day, for 25 years straight,&quot; recalled Headley, chairman of Matthews International Capital Management LLC.<br />
    </p>
  <p>&quot;She came to every Christmas party. She was, basically, our Dorothy.&quot;<br />
    <br />
    Headley's father and Everett also bonded over the fact that both of them were World War II veterans with officer experience.<br />
    <br />
    &quot;Dorothy was, essentially, the executive vice president of the UC system,&quot; Headley said. &quot;She was a quietly incredible person who played a major role in four administrations. Those four presidents were extremely reliant on her expertise and institutional knowledge.&quot;<br />
    <br />
    The endowment will support the management, staffing, instruction, and other needs of UCSC's Global Information Internship Program (GIIP), which mentors students in information technology and social entrepreneurship to advance social justice, sustainable development, peace, and gender equity. Very much in the founding spirit of UCSC, students are given freedom to explore, identify causes close to their hearts, and seek out nonprofit partners.<br />
    <br />
    Headley called GIIP a one-of-a-kind organization &quot;because it trains students to manage and partner with complex projects, organizations, and technologies in ways that many MBAs may promise but rarely deliver.&quot;<br />
    <br />
    Headley said that naming a chair after Everett adds a whole other level of emotional and historic value to an already impactful program.<br />
    <br />
    &quot;Chairs are a great way to strengthen the academic system at a time of decreased resources,&quot; Headley said. &quot;At the same time, if there is anybody you love, with a great passion for something—sciences or the arts, for example—or anybody you want to remember, this is a simple, direct way to make that honor happen.&quot;<br />
    <br />
    Headley also said that naming the chair after &quot;one of the legends of the UC President's office&quot; makes him feel that GIIP is in good hands.<br />
    <br />
    Recently, Headley and Pehl made an additional &quot;naming gift&quot; of $250,000 to GIIP, changing its official name to the Dorothy Everett Program for Global Information and Social Entrepreneurship.<br />
    <br />
    &quot;Dorothy is one of their guardian angels now,&quot; Headley said.</p>
