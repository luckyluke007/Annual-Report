<h2>Sparking a relationship between art and science</h2>
</div>
<div class="bodycontent">Patricia and Rowland Rebele are happy to be part of a history-making project at UC Santa Cruz.<br />
  <br />
  With their $1 million kick-off gift, an important part of UCSC's $300 million fundraising campaign, the Rebeles are helping to make into reality the highly anticipated Institute of the Arts and Sciences, which will offer campus visitors the excitement and discovery of a world class exhibition space, while serving students and faculty as an intellectually vital center of teaching, research, residencies, internships and performance events, and as a social hub for innovative thinking across the university.<br />
  <br />
  The gift, an important element of UCSC's ongoing $300 million fundraiser,  has a deep personal significance for Patricia Rebele, who fulfilled a lifelong dream to go back to college and immerse herself in art—her favorite subject.<br />
  <br />
  She originally aspired to be a fashion illustrator, but art became a hobby once she took a job in the banking industry. But the artistic impulse kept biting at her, and she wound up going back to school, graduating from UCSC in 1988 with a degree in art history. &quot;My poor husband!&quot; she said. &quot;Our three children and I were all attending universities at the same time.&quot;<br />
  <br />
  &quot;What can I say?'' she continued. &quot;Art has always been my passion.&quot;<br />
  <br />
  Now she hopes the institute fires up students' imaginations, just as her own art classes inspired hers. Besides, the Rebeles are both fascinated by the institute's strong interdisciplinary focus, which explores convergences between the sciences and the arts.<br />
  <br />
  Rowland Rebele said he was impressed by a conversation he had with Arts Dean David Yager about artists and how they look at the world differently from other people.<br />
  <br />
  &quot;Artists can look at a planning problem, an engineering problem—a world problem—in a way that can enlighten and inform the scientist,&quot; he said.<br />
  <br />
  Besides, he added, &quot;it's a great feeling, to be a spade-turner in a brand-new project.&quot;<br />
  <br />
  The Rebeles also hope the institute will give locals, and people who live far outside of Santa Cruz, a compelling new reason to visit and explore UCSC.<br />
  <br />
  The institute, they say, will teach visitors about place.<br />
  <br />
  &quot;It is so important to get a handle on the culture of the community and the people who live there,&quot; said Rowland Rebele.</div>
