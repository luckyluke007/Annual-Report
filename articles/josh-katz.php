<h2>Beyond the comfort zone</h2>
</div>
<div class="bodycontent"><p>
  Ask Josh Katz about his experiences at UC Santa Cruz and the 21-year-old art and psychology major will say it's all about pushing himself out of his comfort zone.
<p>First, a psychology class gave the Porter College student such &quot;rich insight&quot; into human behavior, he said, he was able to use that knowledge to drive himself to be more comfortable in his own skin.</p>
  <p>Then came a $700 Irwin Project Grant, which funds student art projects on campus. The grant allowed Katz to expand out from the street-inspired stencil and spray paint he normally used to media that included hand-colored, brightly hued waxes.</p>
  <p>&quot;I got inspired and launched a new trajectory of art-making,&quot; Katz said.</p>
  <p>Finally, Katz pushed his boundaries with a five-month course of study in the Netherlands through UCSC's Education Abroad Program. In the Netherlands, he not only has made friends with people from around the world, but also has learned to be confident in presentation-based classes, raised about $1,600 to help an Ethiopian school get needed supplies, and even hitchhiked to Berlin once.</p>
  <p>&quot;Berlin is covered in murals. It's phenomenal,&quot; said Katz, who found inspiration for his own art during his visit to the capital city.</p>
  <p>The Irwin Grant and UCSC's travel-study program, said Katz, &quot;have been the most wonderful part of my education at UCSC.&quot;</p>
  <p>When Katz graduates in 2015 he plans to move himself out of his comfort zone even further. He wants to create a socially conscious clothing company that will feature his music- and street-inspired art.</p>
  <p> &quot;It's clothing you want to take out for a night,&quot; he said, describing his vision of brightly colored, conceptual apparel.</p>
  <p>Add Katz's desire to have his clothes produced with environmentally friendly and fair labor practices, and also benefit a charity, and that's pushing a boundary even more.<br />
  </p>
