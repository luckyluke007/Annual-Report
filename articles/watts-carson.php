<h2>Gabe Zimmerman's memory lives on in scholarship</h2>
</div>
<div class="bodycontent"><p>
  Carson Watts, an Oakes College sociology major and politics minor, has been selected as the second winner of the Gabriel Zimmerman Memorial Scholarship at UC Santa Cruz.
<p>The award was established in 2011 to support undergraduate students in UCSC's Division of Social Sciences who are passionate about social issues and committed to public service. It honors Gabriel &quot;Gabe&quot; Zimmerman (Stevenson, '02, sociology), who died in the January 2011 shooting that killed five others and wounded 13, including Gabrielle Giffords, a U.S. congresswoman representing the Tucson area. </p>
  <p>Zimmerman served as Giffords' community outreach director and had organized the &quot;congresswoman on the corner&quot; event in Tucson where the shooting occurred.</p>
  <p>Two UCSC alums in San Francisco, moved by Zimmerman's commitment to public service, were the impetus for the scholarship fund that has grown to more than $90,000 with contributions from more than 500 alumni, faculty, staff, community members, and the public. Contributions are still being accepted in hopes of topping the $100,000 level and <a href="https://securelb.imodules.com/s/1069/index-2-column.aspx?sid=1069&amp;gid=1&amp;pgid=780&amp;dids=538">may be made online</a>.</p>
  <p>The alums, Alex Clemens (Porter, '89, international politics) and Jonathan Klein (Merrill, '89, politics) served on the selection committee along with professors Eva Bertram, politics, Mary Beth Pudup, social sciences, and Craig Reinarman, sociology.</p>
  <p>Watts has &quot;displayed remarkable leadership, a profound sense of social responsibility and remarkable intellectual skills while taking on increasingly challenging projects,&quot; said Sociology Department chair Ben Crow.</p>
  <p>Watts, a senior who transferred to UCSC from Columbia Community College, said he hoped to continue Zimmerman's legacy and help &quot;his legacy to spread even further.&quot;</p>
  <p>In December, he returned from Ghana after a five-month study-abroad program in which he conducted an independent research project in sanitation and waste management while working with a waste management company in Old Fadama, an informal settlement of 100,000 people in Accra.</p>
  <p> Earlier, at UCSC,  he mentored transfer students in writing and research and coordinated the Green Building Campaign of the Student Environmental Center, as well as a course to teach and implement environmental sustainability on campus through the Education for Sustainabie Living Program.</p>
  <p>The Social Sciences Division will honor Watts at an Alumni Reunion event at Stevenson College Sunday, April 28, at 1 p.m. in the Fireside Lounge. Last month, he met members of Zimmerman's family and some of his friends at an event in Santa Cruz. Watts said learning how Zimmerman inspired others &quot;gives me energy and encouragement to do more.&quot;</p>
  <p>&quot;I've been busy working as hard as I can, trying to do good things in my time here, so receiving this award recognizing my efforts is an absolute honor,&quot; he said.</p>
  <p>Watts said that after graduation this spring, he plans to do some political organizing and grassroots campaigning to raise money and membership for social justice and environmental groups.<br />
  </p>
