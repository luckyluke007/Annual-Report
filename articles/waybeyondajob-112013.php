<h2>Way beyond a job</h2>
</div>
<div class="bodycontent">
  <p>Joe Konopelski and Nancy Cox-Konopelski love to walk past a corridor lined with photographs of smiling, confident UCSC graduates on their way to great careers.<br />
    <br />
    The students in the photographs are science, technology, engineering, and math (STEM) alums. Some are now medical students, or pursuing advanced degrees in neuroscience, chemistry, computer science, and public health. One is studying aeronautics at Stanford. Another is a researcher at the UCSF Cancer Center.<br />
    <br />
    Each took part in the Academic Excellence (ACE) program, designed to increase the diversity of UCSC students earning STEM degrees through supportive and challenging collaborative work, mentorship, peer support, and staff advising.<br />
    <br />
    When ACE started in 1986, it was groundbreaking, untested, &quot;boldly experimental, 'a nifty idea' kind of program,'' Cox-Konopelski said.<br />
    <br />
    Since then, the program has helped thousands of students make their way through UCSC's rigorous STEM courses.<br />
    <br />
    The Konopelskis' love for the program goes beyond their roles at UCSCâ€”Nancy Cox-Konopelski is the program's director, and Joe Konopelski is a chemistry professor. They are also longtime financial supporters.<br />
    <br />
    &quot;This is way beyond a job,&quot; Nancy Cox-Konopelski said. &quot;The program is<br />
    incredibly important to me.&quot;<br />
    <br />
    She and her husband have used social media to contact students who took part in the program over the past 25 years &quot;and have gone on to do fabulous things,&quot; said Cox-Konopelski. &quot;I have seen students grow. I have heard students say, 'This is what I want to do when I get out there.'&quot;<br />
    <br />
    Like many ACE participants, the Konopelskis were work-study students, and the first in their families to earn a four-year degree. They understand the needs of STEM students &quot;who needed a safe place, a place where they could feel comfortable asking questions, a community of their own.&quot;<br />
    <br />
    Giving helps them ensure that more students will pass through the program. And recently, about 30 ACE alumni have given back, making donations from $10 to $10,000.</p>
