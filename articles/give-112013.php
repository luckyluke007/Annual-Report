<div class="bodycontent">
  <p>UC Santa Cruz is the boldest experiment in the history of public higher education. Our very founding in 1965 was a revolt against the educational status quo.</p>
  <p>Our work impacts public policy and quality of life in countless areas around the world. In an era of risk-aversion, our founding vision is more important than ever: progressive, bold fearless inquiry that benefits both the individual and the planet.</p>
  <p>Profiles in Giving presents the people, places, and programs that uphold the traditions and values of UC Santa Cruz. It also demonstrates the pioneering spirit, proactive work, and philanthropy that distinguish our campus.</p>
  <p><strong>Nobody said we couldn't </strong></p>
  <p>&quot;I was able to ask the question, what is the molecular basis for cancer? And how can we have a precision treatment? Nobody said I couldn't do that,&quot; said David Haussler, director of UCSC's Center for Biomolecular Science and Engineering. &quot;And we built the biggest cancer database in the world—even though we don't even have a med school. Nobody said we couldn't assemble the human genome. So we did!&quot;</p>
  <p><strong>Massive shift</strong></p>
  <p>Josh Stuart, professor of biomolecular engineering at UCSC, helped organize the Pan-Cancer Initiative, an international effort across campus using data sciences that could help bring us closer to a time when doctors treat cancer based on patients' genetic profiles—potentially bringing about a massive shift in thinking about the way cancer treatment works.</p>
  <p><strong>Connecting science and justice</strong></p>
  <p>UCSC's innovative Science and Justice Training Program trains graduate students to think hard about the effects and impacts of their research on society, offering a pathway for science and engineering to connect to social issues and public concerns.</p>
  <p><strong>Saying what must be said</strong></p>
  <p>History professor Dana Frank, one of the top academic experts on Honduras in the United States, testified before the U.S. House of Representatives about human rights and U.S. policy in Honduras. She also contributed a provocative story to the Nation magazine, raising hard but important questions about human rights violations.</p>
  <p><strong>Going in unusual directions</strong></p>
  <p>When biomolecular chemist Harry Noller joined the faculty in 1968, UCSC was an &quot;experimental&quot; campus in the third year of its existence. It was being likened to &quot;an embryonic Cambridge or Oxford,&quot; wrote Noller in his memoir. &quot;The ivory-tower atmosphere and relative isolation of UCSC were in a way liberating; there were not a lot of 'experts' around to discourage you from going in unusual directions.&quot; Noller, who was among the scientists who unraveled the complicated structure of the ribosome, was elected to the National Academy of Sciences in 1992.</p>
