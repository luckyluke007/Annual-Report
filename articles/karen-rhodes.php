<h2>Giving back by paying forward</h2>
</div>
<div class="bodycontent"><p>
  Karen Rhodes (Cowell '77, history) fits the profile of the versatile and determined Banana Slug. Over the years, she's been a student, public information officer, development staff member, and an Alumni Councilor.
<p>But one thing hasn't changed throughout the years: She and her husband, fellow Slug Robert Weiner (College Eight '83, environmental studies), have been consistent, reliable donors to UC Santa Cruz, providing valuable support for Cowell College, the History Department, College Eight, Environmental Studies, and scholarships.</p>
  <p>Last year, the couple joined with other Banana Slugs to start the Environmental Studies Experiential Learning Endowment, which helps fund experiential learning courses that build on classroom studies, while training students to address 21st century environmental challenges.</p>
  <p>&quot;We saw a need to move the initiative forward,&quot; said Rhodes.</p>
  <p>So far, donors have raised $250,000 toward the $1 million goal for this endowment campaign, dubbed &quot;Training Environmental Problem Solvers.&quot;</p>
  <p>Karen Holl, professor and chair of the Environmental Studies Department, praised Rhodes and Weiner for supporting UCSC hands-on learning courses in general as well as the Natural History Field Quarter.</p>
  <p>Holl said these generous alumni gifts will help ensure that environmental studies professors can conduct small field courses that teach critical thinking skills to future generations of UCSC students.</p>
  <p>For Rhodes, it's a question of paying it forward. She found herself referring back to her UCSC experience throughout her working life.</p>
  <p>&quot;I draw on my UC education all the time to keep myself honest and aligned with my core values,&quot; said Rhodes. She added that UCSC was a great place to be for students who felt comfortable with professors, lecturers, books, and fellow students &quot;pushing us to think for ourselves.&quot;</p>
  <p>Such skills came in handy when Rhodes started out as a journalist. Soon after graduation, she started a news program at KZSC. Instead of going to broadcasting school, she learned through experience. &quot;We taught ourselves how to do radio news reporting,&quot; Rhodes said.</p>
  <p>She began freelancing for alternative weeklies in town, covering local politics and environmental issues.</p>
  <p>At KUSP radio, she started working in development, and later she worked in UCSC's University Relations office. She met her husband through mutual friends.</p>
  <p>Rhodes and Weiner left Santa Cruz in 1988, when Robert took a job at UC San Francisco. Rhodes answered a classified ad for an editorial director at UC Berkeley, where she works today.</p>
  <p>But the two of them remain closely connected to UC Santa Cruz through their giving.</p>
  <p>For Rhodes and Weiner, giving comes naturally; it's simply a matter of giving back. But there's also a sense of urgency.</p>
  <p>As the state contribution to the UC system has dwindled &quot;it's even more important for alumni and friends to contribute,&quot; Rhodes said.</p>
  <p>Rhodes has provided for UCSC in her estate planning. She believes it's important to set up the fewest possible restrictions on using the funds.</p>
  <p>&quot;I've always wanted to leave a legacy with my estate and to do something with my assets that is going to have a lasting impact,&quot; said Rhodes. &quot;I want to focus on giving to UCSC in a way that's as unfettered as possible.&quot;</p>
  <p>Gifts of all sizes have an impact, she said.</p>
  <p>&quot;There's an important and catalytic role that can be played in development even if it's not in a big way.&quot;</p>
  <p>Amy Ettinger is a freelance writer based in Santa Cruz. <br />
  </p>
