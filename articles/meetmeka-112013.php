<h2>Using her mind</h2>
</div>
<div class="bodycontent">
  <p>When she was 5, Meka Williams lost her mother to suicide. When she was 8, her father went to prison. </p>
<p>The little girl and her two siblings were taken in by an aunt, who raised them in a cramped apartment in Inglewood, Calif. Gun violence and jail stole some of her friends and, if her freshman year of high school was any indication, it didn't seem likely Williams would go to college.</p>
  <p>But thanks to her aunt, high school mentors, and a drive that rose up inside her like a hunger, Williams is now a sociology major at Stevenson College and plans to graduate in two years.</p>
  <p>The 19-year-old wants to start her own business and become a television personality one day, but right now she is concentrating on getting the most out of her time at UC Santa Cruz.</p>
  <p>One of her most unforgettable lessons, she said, came from a sociology teacher who told Williams's class that if they wanted to make money, they needed to use their minds. </p>
  <p>&quot;It made me realize I have the opportunity to make my life different,&quot; Williams said of that unforgettable lecture. &quot;I was going to have to think hard, work hard, and be creative.&quot;</p>
  <p>Williams does work hard. She not only has two jobs — one mentoring high school students and another connecting foster youth with university resources — but she also is a mentor at the Rosa Parks African American Theme House. </p>
  <p>She also receives financial aid and support from the campus's Educational Opportunity Programs and the Smith Renaissance Society, which assists independent students who are current or former foster youth, runaways, juvenile offenders, orphans, or homeless youths. </p>
  <p>The society is led by a board of UCSC alumni, including Smith Renaissance graduates, and retired faculty and staff.</p>
  <p>&quot;I came to UCSC with only sheets, a blanket and my clothes,&quot; Williams said. &quot;Smith Renaissance gave me a computer.&quot; </p>
  <p>The program also provides her with critical support and mentoring. </p>
  <p>With plans to launch a political web site for young people who aren't in college, Williams is only looking forward.</p>
  <p>&quot;UCSC has allowed me to challenge myself,&quot; she said, &quot;to go beyond my degree.&quot;</p>
