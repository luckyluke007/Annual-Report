<h2>Honoring outstanding teaching in the humanities</h2>
</div>
<div class="bodycontent"><p><div class="caption"><img src="../images/donka.jpg" />
<p>Senior Linguistics major John Zwart, with UCSC professor of <br />
  Linguistics Donka Farkas (Photo by Marissa Fullum-Campbell)</p></div>Linguistics professor Donka Farkas was presented with the 2013 John Dizikes Teaching Award in Humanities yesterday at the Humanities Division's Spring Awards Celebration.</p>
  <p>Established in 2002 to honor outstanding teaching by humanities faculty, the award is named in honor of one of UCSC's founding faculty members.</p>
  <p>The Dizikes teaching award was designed to celebrate the Humanities faculty's commitment to excellence in teaching and its impact on undergraduate students.</p>
  <p>The annual Humanities award comes with an unusual provision. In addition to being honored with a check for $3,000, the winner is entitled to select one undergraduate student to receive a $3,000 scholarship.</p>
  <p>Farkas chose John Zwart, a senior Linguistics major.</p>
  <p>&quot;I chose John because the work he has done over the three quarters we have worked together exemplifies the kind of undergraduate research I hope to help foster,&quot; said Farkas. &quot;It was a joy to see John's engagement and passion.&quot;</p>
  <p>Farkas--whose research is in the field of formal semantics, based on data from Romance languages and Hungarian—reflected on her experience as a teacher at UCSC.</p>
  <p>&quot;People often talk about the 'world of academia' as different from the 'real world' out there.  But when I think that, through teaching, we touch the lives of generation after generation of undergraduate and graduate students, I feel as anchored in the real world as one can be,&quot; said Farkas.</p>
  <p> &quot;The various sides of our academic life–research, undergraduate and graduate teaching–are tightly bound together,&quot; she added. &quot;Enthusiasm comes easily when you talk about a problem in class in the afternoon if you have spent the whole morning thinking and reading about it for your own research, and are basking in the feeling of having made some progress.&quot;</p>
  <p>Now a professor emeritus of American studies, John Dizikes served as Cowell College provost from 1979 to 1983, and mentored thousands of students during his 30-year teaching career.</p>
  <p>He is the author of numerous books, including Opera in America (1993), which won the National Book Critics Circle Award in Criticism.</p>
  <p>A beloved member of the Cowell community, Dizikes and his wife Ann were known for their weekly gatherings of students at the Provost's House before College Nights, and they are still honored today with an annual Dizikes Music Festival at Cowell.<br />
  </p>
