<h2>Child's play = new research</h2>
</div>
<div class="bodycontent"><p><div class="caption"><img src="../images/eec-grant.jpg" />
<p>UC Santa Cruz Early Education Services Director Sohyla Fathi,<br />
  left, and psychology professor Catherine Cooper explain the program <br />
  to Giannini Fund trustees George Vera, Bruce Hopkins, and Betsy<br />
  Buchalter Adler during their visit to the campus childcare center.<br />
  (Photo by Guy Lasnier)</p></div>A $165,000 grant to Early Education Services at UC Santa Cruz has enabled the program to extend childcare hours for students' children and provide an observation room for developmental psychology research.
  <p>The grant comes from the Claire Giannini Fund founded in 1998 after the death of Claire Giannini Hoffman, daughter of A.P. Giannini, founder of the Bank of America.</p>
  <p>Funding has provided for extended hours over the summer, hours that will continue through the current academic year and summer 2013. The longer hours makes it much easier for students to take early or late classes, said Dave Keller, UCSC director of housing and facility services.</p>
  <p>The observation room at the UCSC Child Study Center means that undergraduate and graduate students in advanced infant development classes can observe the behavior of young children more easily. Previously, the university didn't have a facility to observe children's behavior in a research setting.</p>
  <p>&quot;A great university should have a great child and family center,&quot; said Catherine Cooper, quoting her father. Cooper, emerita professor of psychology at UCSC, was instrumental in securing the grant and establishing the observation site. The facility is an opportunity to integrate research, teaching, and service at UCSC, she said.</p>
  <p>It is the first step toward an ultimate goal of providing childcare for faculty, staff and students and a rich child development/family study center.</p>
  <p>The Giannini Fund's three trustees visited the center earlier this month. Trustee Betsy Buchalter Adler, a Cowell alumna, and trustee on the UC Santa Cruz Foundation, said she asked childcare officials what were the top five things Early Education Services needed. Numbers one and two were extended hours, she was told, and three was to equip a room for child observation.</p>
  <p>Adler, who has retired as senior counsel at her firm Adler &amp; Colvin in San Francisco, specializes in advising non-profits. Her practice focuses on grant-making charities, including family, company, and community foundations; charitable governance issues; and international philanthropy.</p>
  <p>She said she and the Giannini Fund's two other trustees are focusing on programs that serve children under 18 and on early intervention programs for youths with vision or hearing impairments.<br />
  </p>
